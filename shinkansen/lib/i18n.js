// i18n.js — Shinkansen 多語言模組
// 支援 zh-TW（繁體中文）、zh-CN（简体中文）、ja（日本語）
//
// ES module 用法（background / popup / options）：
//   import { t, setLocale, getLocale, applyLocale } from './i18n.js';
//
// Content script 用法（透過 content-ns.js 初始化 SK.t）：
//   SK.t('key', arg1, arg2)

import { browser } from './compat.js';

// ─── 當前語言 ────────────────────────────────────────────
let _locale = 'zh-TW';

export function getLocale() { return _locale; }
export function setLocale(locale) {
  if (STRINGS[locale]) _locale = locale;
}

/** 從 storage 讀取 uiLocale 並設定 */
export async function initLocale() {
  try {
    const { uiLocale } = await browser.storage.sync.get('uiLocale');
    if (uiLocale && STRINGS[uiLocale]) _locale = uiLocale;
  } catch { /* 靜默 fallback zh-TW */ }
}

/**
 * 取得翻譯字串。支援佔位符 {0}, {1}, ...
 * @param {string} key
 * @param {...any} args
 */
export function t(key, ...args) {
  const table = STRINGS[_locale] || STRINGS['zh-TW'];
  let s = table[key] ?? STRINGS['zh-TW'][key] ?? key;
  for (let i = 0; i < args.length; i++) {
    s = s.replace(`{${i}}`, args[i]);
  }
  return s;
}

/**
 * 遍歷 DOM，將 data-i18n 屬性的元素替換為對應翻譯。
 * data-i18n="key"           → textContent
 * data-i18n-placeholder="key" → placeholder
 * data-i18n-title="key"     → title
 * @param {Element} root
 */
export function applyLocale(root = document) {
  for (const el of root.querySelectorAll('[data-i18n]')) {
    const key = el.getAttribute('data-i18n');
    if (key) el.textContent = t(key);
  }
  for (const el of root.querySelectorAll('[data-i18n-placeholder]')) {
    const key = el.getAttribute('data-i18n-placeholder');
    if (key) el.placeholder = t(key);
  }
  for (const el of root.querySelectorAll('[data-i18n-title]')) {
    const key = el.getAttribute('data-i18n-title');
    if (key) el.title = t(key);
  }
}

// ─── 精簡版字串表（供 content script 內嵌使用） ──────────
// content-ns.js 無法 import ES module，此函式返回當前語言的 content script 字串
export function getContentStrings() {
  const table = STRINGS[_locale] || STRINGS['zh-TW'];
  const keys = Object.keys(table).filter(k => k.startsWith('cs_'));
  const result = {};
  for (const k of keys) result[k] = table[k];
  return result;
}

